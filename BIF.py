#We'll discuss here some of the built in functions which are lists, dictionaries, set and tuple.
#1. Lists
L = list()
print(L)
L = [1,2,3]
print(L)
#2. Dictionaries
D = dict()
print(D)
D = {'Name' : 1, 'class' : 12}
print(D)
#3. Sets
S = set()
print(S)
S = {1,2,3}
print(S)
#4. Tuples
T = tuple()
print(T)
T = (1,2,3)
print(T)